package com.manthan.hotelbill.jdbc.assesment.bean;

public class FoodBean {
	
	int Item_code;
	String Food_Name;
	double Price;
	public int getItem_code() {
		return Item_code;
	}
	public void setItem_code(int item_code) {
		Item_code = item_code;
	}
	public String getFood_Name() {
		return Food_Name;
	}
	public void setFood_Name(String food_Name) {
		Food_Name = food_Name;
	}
	public double getPrice() {
		return Price;
	}
	
	

}
